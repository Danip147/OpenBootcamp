package teoriaJava;

public class FoorLoop {

	public static void main(String[] args) {
		
		// Bucle for
		// Usaremos for cuando sepamos el número de iteraciones
		
		for (int i = 0; i < 10; i++) {
			System.out.println(i + " Hola mundo");
		}
		
		System.out.println("");
		
		// Empieza en 1 y llega hasta el número 10
		for (int i = 1; i <= 10; i++) {
			System.out.println(i + " Hola mundo");
		}

	}

}
