package tiposDeDatos;

public class TiposDatos {

	public static void main(String[] args) {
		          

		// enteros 
		byte number1 = 1;  // 1 byte
		short number2 = 2; // 2 byte 
		int number3 = 3;   // 3 byte
		long number4 = 4;  // 8 byte
		
		// punto flotante
		float decimal = 4.9f;
		double decimal2 = 4.9;
		
		// caracter
		char caracter1 = 'a';
		
		//boolean
		boolean verdadero = true;
		boolean falso = false;
		
		// cadenas de texto
		String nombre = "Daniel";
		String apellido = "Porras";
		
		// tipos envoltorio
		Integer numero = null;
		Long numero2 = 2L;
		
		
	}
	

}
